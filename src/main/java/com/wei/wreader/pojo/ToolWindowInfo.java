package com.wei.wreader.pojo;

public class ToolWindowInfo {
    private String searchTitle;
    private String fontSizeSubTitle;
    private String fontSizeAddTitle;
    private String chapterListTitle;
    private String prevChapterTitle;
    private String nextChapterTitle;

    public String getSearchTitle() {
        return searchTitle;
    }

    public void setSearchTitle(String searchTitle) {
        this.searchTitle = searchTitle;
    }

    public String getFontSizeSubTitle() {
        return fontSizeSubTitle;
    }

    public void setFontSizeSubTitle(String fontSizeSubTitle) {
        this.fontSizeSubTitle = fontSizeSubTitle;
    }

    public String getFontSizeAddTitle() {
        return fontSizeAddTitle;
    }

    public void setFontSizeAddTitle(String fontSizeAddTitle) {
        this.fontSizeAddTitle = fontSizeAddTitle;
    }

    public String getChapterListTitle() {
        return chapterListTitle;
    }

    public void setChapterListTitle(String chapterListTitle) {
        this.chapterListTitle = chapterListTitle;
    }

    public String getPrevChapterTitle() {
        return prevChapterTitle;
    }

    public void setPrevChapterTitle(String prevChapterTitle) {
        this.prevChapterTitle = prevChapterTitle;
    }

    public String getNextChapterTitle() {
        return nextChapterTitle;
    }

    public void setNextChapterTitle(String nextChapterTitle) {
        this.nextChapterTitle = nextChapterTitle;
    }
}
